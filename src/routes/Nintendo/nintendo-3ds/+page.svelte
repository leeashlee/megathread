        >
            3DS/CIA Decryption Instructions
        </h2>
        <p class="leading-7 [&:not(:first-child)]:mt-6">
            If Citra says the ROM is encrypted, use <a
                href="https://gbatemp.net/download/batch-cia-3ds-decryptor.35098/"
                class="text-primary font-medium underline underline-offset-4 md:text-base hover:bg-primary hover:text-primary-foreground"
                >Batch CIA-3DS Decryptor</a
            >
            to decrypt it.<br />
            If you would like to get a proper No-Intro hash from the converted file,
            use
            <a
                href="https://archive.org/download/pkmn_collection/3DS%20%2B%20CIA%20Rom%20Script.rar"
                class="text-primary font-medium underline underline-offset-4 md:text-base hover:bg-primary hover:text-primary-foreground"
                >this script</a
            >.<br />
            Or, add the following AES keys to Citra from
            <a
                href="https://pastebin.com/vRy8c6JP"
                class="text-primary font-medium underline underline-offset-4 md:text-base hover:bg-primary hover:text-primary-foreground"
                >here</a
            >
            or
            <a
                href="https://web.archive.org/web/20220707222654/https://pastebin.com/tBY6RHh4"
                class="text-primary font-medium underline underline-offset-4 md:text-base hover:bg-primary hover:text-primary-foreground"
                >here</a
            >. <br /> Click download on Pastebin to download the text in the file
            as a TXT. Then, follow the installation instructions below for your OS.
        </p>
        <h3 class="mt-8 scroll-m-20 text-2xl font-semibold tracking-tight">
            Windows Installation Instructions
        </h3>
        <p class="leading-7 [&:not(:first-child)]:mt-6">
            Add the downloaded Pastebin TXT file to <code
                class="bg-muted relative rounded px-[0.3rem] py-[0.2rem] font-mono text-sm font-semibold"
            >
                C:/Users/(Your Username)/AppData/Roaming/Citra/sysdata
            </code>
            and make sure it is named
            <code
                class="bg-muted relative rounded px-[0.3rem] py-[0.2rem] font-mono text-sm font-semibold"
            >
                aes_keys.txt
            </code>.
        </p>
        <h3 class="mt-8 scroll-m-20 text-2xl font-semibold tracking-tight">
