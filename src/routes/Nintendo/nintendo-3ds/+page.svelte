            >
                ~/.local/share/citra-emu/sysdata
            </code>
            and make sure it is named
            <code
                class="bg-muted relative rounded px-[0.3rem] py-[0.2rem] font-mono text-sm font-semibold"
            >
                aes_keys.txt
            </code>. For more information, refer to the
